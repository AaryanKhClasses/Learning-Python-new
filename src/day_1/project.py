# Project for a Band Name Generator.
# The first thing to do is to make a greeting for your program.
print("Hello! Welcome to the Band Name Generator!")

# Then ask the user for the name of the city they grew up in.
city = input("What's the name of the city you grew up in?\n")

# Then ask the user for the name of their pet.
pet = input("What's the name of your pet?\n")

# Then combine the two names into a new name for the band and give them their new band's name!
band_name = city + " " + pet
print("Your new band name is '" + band_name + "'d!")

